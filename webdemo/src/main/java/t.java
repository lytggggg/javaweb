public class t {
    private int d;
    private String name;


    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "t{" +
                "d=" + d +
                ", name='" + name + '\'' +
                '}';
    }



}
